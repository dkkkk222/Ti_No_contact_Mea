//
//Lab3 ISR code. MPPT voltage loop and current loop closed

#pragma FUNC_ALWAYS_INLINE(MPPTDCDC_runISR1_lab3)
static inline void MPPTDCDC_runISR1_lab3(void)
{
    //
    // Read ADC sampled voltage and current values
    //
    MPPTDCDC_readCurrentAndVoltageSignals();
    //
    EMAVG_MACRO(MPPTDCDC_iL_sensed_pu, MPPTDCDC_iL_sensed_Avg_pu, (float)0.001);//f_cutoff = 8.4Hz
    EMAVG_MACRO(MPPTDCDC_vPanel_sensed_pu, MPPTDCDC_vPanel_sensed_Avg_pu, (float)0.001);//f_cutoff = 8.4Hz
    //Multiplier = (2*pi*f_cutoff)/f_sampling
    //

    //
    // Clear the PWM trips when MPPTDCDC_clearPWMTrip
    // is written to '1' from the watch window
    //
    if(MPPTDCDC_clearPWMTrip == 1U)
    {
            MPPTDCDC_clearPWMTrip = 0;
            MPPTDCDC_clearAllPWMTrips();
            MPPTDCDC_updateDuty = 1U;
    }

    if (MPPTDCDC_updateDuty)
    {


        //Voltage control loop

        //This volt loop has +ve feedback (compared to the conventional -ve feedback) since for higher Vpanel, i.e., panel volt
        //we want higher control output, i.e., in this case higher boost current and vice versa. Thus the volt loop controller (MPPTDCDC_GV_RUN)
        //connections below are reversed for feedback and reference signals
        //PV panel reference(MPPTDCDC_Vpvref_Out) is connected to the controller feedback.
        //MPPTDCDC_vPanel_sensed_pu is the PV panel feedback and is connected to the controller reference.

        //MPPTDCDC_Vpvref_Out is the output of the MPPT algorithm. This volt loop controller regulates it's out to maintain the input
        //of the boost converter at MPPTDCDC_Vpvref_Out

        //MPPTDCDC_idRef_pu is the output of the controller and is used as Reference for inner current loop

#if     MPPT_DCDC_SFRA_TYPE == MPPT_DCDC_SFRA_VOLTAGE

        MPPTDCDC_idRef_pu = MPPTDCDC_GV_RUN(&MPPTDCDC_gv_vBus,SFRA_F32_inject(MPPTDCDC_vPanel_sensed_pu),MPPTDCDC_Vpvref_Out);
        MPPTDCDC_gv_vBus_out = MPPTDCDC_idRef_pu;
        SFRA_F32_collect((float32_t *)&MPPTDCDC_gv_vBus_out, (float32_t *)&MPPTDCDC_Vpvref_Out);

#else

        MPPTDCDC_idRef_pu = MPPTDCDC_GV_RUN(&MPPTDCDC_gv_vBus,MPPTDCDC_vPanel_sensed_pu,MPPTDCDC_Vpvref_Out);

#endif

        //Current control loop
        MPPTDCDC_runCurrentLoop();

        MPPTDCDC_duty_A = MPPTDCDC_gi_id_out;

        MPPTDCDC_duty_B = MPPTDCDC_gi_id_out;

        MPPTDCDC_updateBoostPWM(MPPTDCDC_duty_A, MPPTDCDC_duty_B);


//Enable MPPT when DC Bus is higher than PV panel input

        if(MPPTDCDC_vBus_sensed_pu > 0.30f)
        {
            MPPTDCDC_MPPT_ENABLE=1;
            MPPTDCDC_incc1.mppt_enable = 1;
        }
        else
        {
            MPPTDCDC_MPPT_ENABLE=0;
            MPPTDCDC_incc1.mppt_enable = 0;
        }

    }

    MPPTDCDC_vBus_sensed=MPPTDCDC_vBus_sensed_pu*MPPT_DCDC_VDCBUS_MAX_SENSE;
    MPPTDCDC_vBus_sensed_filter=Low_Pass_Filter*(MPPTDCDC_vBus_sensed_filter-MPPTDCDC_vBus_sensed)+MPPTDCDC_vBus_sensed;

    MPPTDCDC_HAL_clearPWMInterruptFlag(MPPT_DCDC_ISR1_TRIG_BASE);
}




