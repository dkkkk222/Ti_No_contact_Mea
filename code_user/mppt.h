//
// Read all currents and voltage parameters
//
#pragma FUNC_ALWAYS_INLINE(MPPTDCDC_readCurrentAndVoltageSignals)
static inline void MPPTDCDC_readCurrentAndVoltageSignals()
{

    MPPTDCDC_vOut_sensed_pu = ((float32_t)MPPT_DCDC_HAL_VOUT_FB *
                               MPPT_DCDC_ADC_PU_SCALE_FACTOR);


    MPPTDCDC_iL_sensed_pu = ((float32_t)MPPT_DCDC_HAL_IL_FB *
                                MPPT_DCDC_ADC_PU_SCALE_FACTOR);


    MPPTDCDC_vPanel_sensed_pu = ((float32_t)MPPT_DCDC_VPANEL_FB *
                              MPPT_DCDC_ADC_PU_SCALE_FACTOR);

    MPPTDCDC_vBus_sensed_pu = ((float32_t)MPPT_DCDC_VBUS_FB *
                              MPPT_DCDC_ADC_PU_SCALE_FACTOR);

}

//
// Converts per unit measurements to absolute values
//
static inline void MPPTDCDC_puToVal_conversion()
{
    MPPTDCDC_guivOut_inst =  MPPTDCDC_vOut_sensed_pu * MPPT_DCDC_VOUT_MAX_SENSE;

    MPPTDCDC_guiiL_inst =  MPPTDCDC_iL_sensed_pu * MPPT_DCDC_IL_MAX_SENSE;

    MPPTDCDC_guiiL_Avg = MPPTDCDC_iL_sensed_Avg_pu* MPPT_DCDC_IL_MAX_SENSE;

    MPPTDCDC_guiVpanel =  MPPTDCDC_vPanel_sensed_pu * MPPT_DCDC_VPANEL_MAX_SENSE;

    MPPTDCDC_guiVbus =  MPPTDCDC_vBus_sensed_pu * MPPT_DCDC_VDCBUS_MAX_SENSE;
}

//
// Updates the PWM duty values for 2 phases
//
#pragma FUNC_ALWAYS_INLINE(MPPTDCDC_updateBoostPWM)

static inline void MPPTDCDC_updateBoostPWM(float32_t dutyA,
                                     float32_t dutyB)
{

    dutyA = (dutyA > MPPT_DCDC_DUTY_MAX) ? MPPT_DCDC_DUTY_MAX : dutyA;

    dutyA = (dutyA < MPPT_DCDC_DUTY_MIN) ? MPPT_DCDC_DUTY_MIN : dutyA;

    dutyB = (dutyB > MPPT_DCDC_DUTY_MAX) ? MPPT_DCDC_DUTY_MAX : dutyB;
    dutyB = (dutyB < MPPT_DCDC_DUTY_MIN) ? MPPT_DCDC_DUTY_MIN : dutyB;

    MPPTDCDC_HAL_updateCMPA(MPPT_DCDC_HIGH_FREQ_PWM_A_BASE, dutyA);

    MPPTDCDC_HAL_updateCMPA(MPPT_DCDC_HIGH_FREQ_PWM_B_BASE, dutyB);
}
//
//**************code for LLC PWM************************
#pragma FUNC_ALWAYS_INLINE(MPPTDCDC_updateLLCPWM)
static inline void MPPTDCDC_updateLLCPWM(float32_t llc_prd)
{

    llc_prd = (llc_prd > 0.5f*MPPT_DCDC_LLC_PWM_PRD_MAX) ? 0.5f*MPPT_DCDC_LLC_PWM_PRD_MAX : llc_prd;
    llc_prd = (llc_prd < 0.5f*MPPT_DCDC_LLC_PWM_PRD_MIN) ? 0.5f*MPPT_DCDC_LLC_PWM_PRD_MIN : llc_prd;

    MPPTDCDC_HAL_updateTBPRD(MPPT_DCDC_LLC_PWM_BASE, llc_prd);
}
//***********end LLC****************************
