//
// Current Control Loop Execute Function
//
#pragma FUNC_ALWAYS_INLINE(MPPTDCDC_runCurrentLoop)
static inline void MPPTDCDC_runCurrentLoop(void)
{

//
// Closed loop operation with SFRA enabled
//
#if MPPT_DCDC_SFRA_TYPE == MPPT_DCDC_SFRA_CURRENT

        MPPTDCDC_gi_id_out = MPPTDCDC_GI_RUN(&MPPTDCDC_gi_id,
                                           SFRA_F32_inject(MPPTDCDC_idRef_pu),
                                           MPPTDCDC_iL_sensed_pu);
        SFRA_F32_collect(&MPPTDCDC_gi_id_out, &MPPTDCDC_iL_sensed_pu);

#else
        // Closed loop operation with SFRA disabled
        //
        MPPTDCDC_gi_id_out = MPPTDCDC_GI_RUN(&MPPTDCDC_gi_id,MPPTDCDC_idRef_pu,MPPTDCDC_iL_sensed_pu);
#endif

        MPPTDCDC_gi_id_out = (MPPTDCDC_gi_id_out > 1.0f) ? 1.0f : MPPTDCDC_gi_id_out;
        MPPTDCDC_gi_id_out = (MPPTDCDC_gi_id_out < 0.0f) ? 0.0f : MPPTDCDC_gi_id_out;

}

//
// Lab1 ISR code
//
#pragma FUNC_ALWAYS_INLINE(MPPTDCDC_runISR1_lab1)
static inline void MPPTDCDC_runISR1_lab1(void)
{
    //
    // Read ADC sampled voltage and current values
    //
    MPPTDCDC_readCurrentAndVoltageSignals();
    //
    EMAVG_MACRO(MPPTDCDC_iL_sensed_pu, MPPTDCDC_iL_sensed_Avg_pu, (float)0.000528);
    EMAVG_MACRO(MPPTDCDC_vPanel_sensed_pu, MPPTDCDC_vPanel_sensed_Avg_pu, (float)0.000528);//f_cutoff = 4.2Hz
    //Multiplier = (2*pi*f_cutoff)/f_sampling
    //
    //
    // Clear the PWM trips when MPPTDCDC_clearPWMTrip
    // is written to '1' from the watch window
    //
    if(MPPTDCDC_clearPWMTrip == 1U)
    {
      MPPTDCDC_clearPWMTrip = 0;
      MPPTDCDC_clearAllPWMTrips();
      MPPTDCDC_updateDuty = 1U;
    }


    if (MPPTDCDC_updateDuty == 1U)
    {
        MPPTDCDC_updateDuty = 0;
        MPPTDCDC_updateBoostPWM(MPPTDCDC_duty_A, MPPTDCDC_duty_B);
        MPPTDCDC_updateLLCPWM(MPPTDCDC_llc_prd);
    }

    MPPTDCDC_HAL_clearPWMInterruptFlag(MPPT_DCDC_ISR1_TRIG_BASE);
}

